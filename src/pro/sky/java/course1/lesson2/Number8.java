package pro.sky.java.course1.lesson2;

public class Number8 {
    public static void main(String[] args) {
        int x = 6;
        int y = 5;
        int z = 2;
        // x - число сотен, y - число десятков, z - число единиц в числе a
        int a = 100 * x + 10 * y + 1 * z;
        int b = y;
        System.out.println("Число a = " + a);
        System.out.println("Число b = " + b);
    }
}
