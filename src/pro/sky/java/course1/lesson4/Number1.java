package pro.sky.java.course1.lesson4;

public class Number1 {
    public static void main(String[] args) {
        int i = 0;
        while (i <= 9) {
            i++;
            System.out.print(i + " ");
        }
        System.out.println();

        for (int a = 10; a > 0; a = a - 1) {
            System.out.print(a + " ");
        }
    }
}
